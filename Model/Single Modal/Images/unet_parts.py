class DoubleConv(nn.Module):
    """(convolution => [BN] => ReLU) * 2"""
    def __init__(self, in_channels, out_channels):
        super().__init__()
        self.double_conv = nn.Sequential(
            nn.Conv2d(in_channels, out_channels, kernel_size=3, padding=1),
            nn.BatchNorm2d(out_channels),
            nn.ReLU(inplace=True),
            nn.Conv2d(out_channels, out_channels, kernel_size=3, padding=1),
            nn.BatchNorm2d(out_channels),
            nn.ReLU(inplace=True)
        )

    def forward(self, x):
        return self.double_conv(x)
    
def crop_tensor(target_tensor, tensor_to_crop):
    """
    Crop the tensor_to_crop to match the size of target_tensor.
    Args:
        target_tensor (torch.Tensor): The tensor you want to match.
        tensor_to_crop (torch.Tensor): The tensor that needs cropping.
    Returns:
        torch.Tensor: Cropped tensor.
    """
    target_size = target_tensor.size()[2:]
    crop_size = tensor_to_crop.size()[2:]

    # Calculate cropping start indices
    delta_height = crop_size[0] - target_size[0]
    delta_width = crop_size[1] - target_size[1]
    start_height = delta_height // 2
    start_width = delta_width // 2

    # Perform cropping
    cropped_tensor = tensor_to_crop[:, :, start_height:start_height + target_size[0], start_width:start_width + target_size[1]]
    return cropped_tensor

class Down(nn.Module):
    """Downscaling with maxpool then double conv"""
    def __init__(self, in_channels, out_channels):
        super().__init__()
        self.maxpool_conv = nn.Sequential(
            nn.MaxPool2d(2),
            DoubleConv(in_channels, out_channels)
        )

    def forward(self, x):
        return self.maxpool_conv(x)
    
class Up(nn.Module):
    """Upscaling then double conv"""
    def __init__(self, in_channels, out_channels, bilinear=True):
        super().__init__()

        # Calculate the number of input channels for the convolutional layer after concatenation
        # It's the sum of in_channels (from the upsampled feature map) and out_channels (from the skip connection)
        conv_in_channels = in_channels + out_channels

        if bilinear:
            self.up = nn.Upsample(scale_factor=2, mode='bilinear', align_corners=True)
            # Adjust the in_channels for the convolution to match the concatenated feature map
            self.conv = DoubleConv(conv_in_channels, out_channels)
        else:
            self.up = nn.ConvTranspose2d(in_channels // 2, in_channels // 2, kernel_size=2, stride=2)
            # Adjust the in_channels for the convolution to match the concatenated feature map
            self.conv = DoubleConv(conv_in_channels, out_channels)

    def forward(self, x1, x2):
        x1 = self.up(x1)
        diffY = x2.size()[2] - x1.size()[2]
        diffX = x2.size()[3] - x1.size()[3]
        x1 = F.pad(x1, [diffX // 2, diffX - diffX // 2,
                        diffY // 2, diffY - diffY // 2])
        x = torch.cat([x2, x1], dim=1)
        return self.conv(x)
    
class DoubleConv(nn.Module):
    """(convolution => [BN] => ReLU) * 2"""
    def __init__(self, in_channels, out_channels, mid_channels=None):
        super(DoubleConv, self).__init__()
        if not mid_channels:
            mid_channels = out_channels
        self.double_conv = nn.Sequential(
            nn.Conv2d(in_channels, mid_channels, kernel_size=3, padding=1),
            nn.BatchNorm2d(mid_channels),
            nn.ReLU(inplace=True),
            nn.Conv2d(mid_channels, out_channels, kernel_size=3, padding=1),
            nn.BatchNorm2d(out_channels),
            nn.ReLU(inplace=True)
        )

    def forward(self, x):
        return self.double_conv(x)


